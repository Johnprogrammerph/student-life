    <div class="content">
        <p>As social networking sites (SNSs) become increasingly pervasive among students, educational information
            system (EIS) researchers begin to highlight the potential negative effect of SNSs for students. Despite such
            interest, scholars have paid little attention to the underlying mechanism of EIS to date. By employing two
            well-known theoretical models (namely, the transactional model of stress and the stressor-strain-outcome
            framework), we investigated the determinants of excessive SNS usage and how excessive use induces cognitive
            distraction and consequently influences the academic performance of the students. Using a survey of 505
            university undergraduate students who are SNS users and the objective data of their cumulative grade point
            average, we confirmed four technology characteristics (socialization, social comparisons, enjoyment, and
            information seeking) as determinants of excessive use. Excessive use of SNS causes cognitive distraction
            among students, thereby affecting academic performance. Results also showed the interaction effect of the
            boundary condition, that is, cognitive-behavioral control, which mitigates the deleterious effect of
            excessive use in relation to cognitive distractions. The outcomes offer significant implications for the
            strategizing of managers and researchers to reduce psychological problems among SNS users.</p>
