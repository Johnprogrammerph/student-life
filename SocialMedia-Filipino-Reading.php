        <p>Profusely widespread use of social media opened multiple reading opportunities for learners. However,
            educators have growing concern that excessive use of social media compromises learners' reading competence
            and, eventually, academic performance. The current work aimed to examine the extent to which social media
            use may affect the university students' (N=101) reading attitudes and competence, which are the backbone of
            scholastic performance. A mixed-methods approach was applied in the study. The data collected were analyzed
            using version 26 of the Statistical Package for Social Sciences (SPSS) software. Interview answers were
            qualitatively analyzed. Results indicate that social media platforms (SMPs) were least used for educational
            purposes. Students showed positive reading attitudes emphasizing their accorded importance to reading and
            finding happiness to learn new things. However, as they managed to read posts daily, lesser inclination was
            accorded to productive reading attitudes such as finding time to read during vacant hours and having
            enthusiasm towards advanced reading and reading with friends. Overall, reading competence indicated
            ineptitude. This study provides insights into the context of social media use in shaping learners' reading
            attitudes and affecting their level of reading competence.</p>
