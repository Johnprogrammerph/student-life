        <p>Social media is the most popular platform for social networking of today's generation. As social media sites,
            such as Facebook, Twitter, YouTube, online gaming, etc., continue to grow, it becomes a part of every
            person's daily life, especially college students. The dependency of students on social media has reached
            such a level that every student cannot think about the direction of their growth without social media.
            Hence, the dependency of every student on social media is now leading to addiction. This research aimed to
            determine the effects of social media on the academic performance and behavior of Grade 11 GAS students at
            Bestlink College of the Philippines. Social media has negative effects on every student. Many students find
            it difficult to go about their normal lives without using social media. However, despite the possible
            negative effects of using social media, we cannot deny its contribution to our daily lives. Social media has
            become essential in today's generation because we are dealing with the 22nd Century learning process. This
            study utilized normative descriptive method design in collecting the necessary data in answering the main
            and specific problems stated in Chapter 1. As a result, social media positively affects students' academic
            performance by allowing them to easily communicate with others. However, the negative effect is that
            students lose focus. The positive effect on their behavior is that students increase their knowledge, while
            the negative effect is addiction, which can lead to insomnia. The problem encountered by the respondents in
            using social media is the lack of concentration in their daily activities. Based on the summary of findings,
            in terms of academic performance, the positive effect of social media is the ease of communication with each
            other, while the negative effect is losing focus.</p>
